function вивестиРоки(кількість) {
    if (кількість % 10 === 1 && кількість !== 11) {
        return кількість + ' рік';
    } else if (кількість % 10 >= 2 && кількість % 10 <= 4 && (кількість < 10 || кількість > 20)) {
        return кількість + ' роки';
    } else {
        return кількість + ' років';
    }
}

// Перевірка зазначених даних
const тестовіДані = [1, 26, 31, 48, 51, 50, 81, 87, 94, 100, 0];

тестовіДані.forEach(кількість => {
    console.log(вивестиРоки(кількість))
})